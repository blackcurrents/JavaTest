public class yunsuanfu {
    public static void main(String[] args) {
        System.out.println(2/5);//0
        System.out.println(2/(float)5);//0.4
        System.out.println((float)2/5);//0.4
        System.out.println((float)(2/5));//0.00
        System.out.println(11.5%2);//java可以小数取余
        System.out.println("___________");
        float a=10.1f;
        int b=20;
        System.out.println(a==b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        System.out.println(a!=b);
    }
}
